# Python Casting

x = int(32)   # x will be 32
y = int(2.338) # y will be 2
z = int("65") # z will be 65

print(x)
print(y)
print(z)


x = float(100000)     # x will be 100000.0
y = float(2.800000)   # y will be 2.8
z = float("39899")   # z will be 39899.0
w = float("4.299") # w will be 4.299

print(x)
print(y)
print(z)
print(w)

x = str("s1232") # x will be 's1232'
y = str('gayaaa')    # y will be 'gayaaa'
z = str(3.00909090)  # z will be '3.00909090'
h = str(90) #h will be '90'
print(x)
